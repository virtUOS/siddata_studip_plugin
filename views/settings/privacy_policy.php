        <h1>Datenschutzerklärung</h1>
        <p>
            Mit dieser Datenschutzerklärung soll den Nutzenden des Siddata-Plugins transparent gemacht werden, wie und in welchem Umfang sowie zu welchem Zweck Daten erhoben und wie diese verarbeitet werden.
        </p>
        <p>
            Grundsätzlich werden bei der Nutzung von Siddata alle anfallenden Daten in pseudonymisierter Form auf dem Server gespeichert und sind somit den Nutzenden nicht mehr zuordenbar. In der <a href="<?= $controller->link_for('settings/privacy_data') ?>">Datenfreigabe</a> kannst du einstellen, welche Daten zu diesen pseudonymisierten Daten gehören sollen. Unter 1 ist dies weiter erläutert.</p>
        <p>
            Es gibt aber auch einige Module bei denen die Daten in nicht-pseudonymisierter Form abgespeichert werden müssen. Diese sind im Punkt 2 beschrieben.
        </p>
        <p>
            Die Nutzung des Plugins und das Bereitstellen von Daten für das SIDDATA Projekt erfolgen freiwillig. Rechtsgrundlage für die Datenverarbeitung solcher freiwilligen Angaben ist die Einwilligung gem. Art. 6 Abs. 1 lit. a) DSGVO. Die Einwilligung in die Verarbeitung der freiwilligen Angaben gemäß Art. 6 Abs. 1 lit. a) DSGVO können Sie jederzeit mit Wirkung für die Zukunft widerrufen. In diesem Fall werden wir diese Daten nicht mehr weiterverarbeiten und diese Daten löschen. Die Rechtmäßigkeit der Datenverarbeitung bis zum Widerruf wird durch diesen nicht berührt. Der Widerruf kann selbstständig durch ein Entfernen der freiwilligen Angaben aus dem System erfolgen.
        </p>

        <h2>1. Verarbeitung pseudonymisierter Daten</h2>
        <p>
            Daten, die über die <a href="<?= $controller->link_for('settings/privacy_data') ?>">Datenfreigabe</a> mit SIDDATA geteilt werden, werden zur Analyse der Nutzung von Siddata-Funktionen zwecks Weiterentwicklung und Forschung genutzt. Diese Daten werden in pseudonymisierter Form gespeichert und sind den Nutzenden nicht mehr zuordenbar.
        </p>
        <p>
            Solche Datennutzungsszenarien sind beispielsweise:
        </p>
        <ul>
            <li>Aktivierte Funktionen lassen Rückschlüsse auf die Nachfrage nach Funktionen zu.</li>
            <li>Ob eine Empfehlung genutzt wurde, lässt Rückschlüsse darüber zu, ob sie hilfreich war. Dieses Wissen hilft die KI zu trainieren.</li>
            <li>Die Untersuchung von Forschungsfragen zur Nutzung und Wirksamkeit des Assistenten durch die Analyse von Daten über die Nutzung von Siddata-Funktionen und -Aktionen und zusätzlichen freiwilligen Befragungen.</li>
        </ul>
        <p>
            Empfängerinnen und Empfänger dieser Daten sind Mitarbeitende des SIDDATA Forschungsprojekts.
        </p>
        <p>
            Diese Daten werden unverschlüsselt gespeichert und bis zum Ende der Projektlaufzeit am 30.04.2022 an der jeweiligen Universität im Backend vorgehalten und dann automatisiert gelöscht.
        </p>

        <h2>2. Verarbeitung personenbezogener Daten</h2>
        <p>
            Im Folgenden werden Module beschrieben bei denen auf freiwilliger Basis personenbezogene Daten eingegeben werden können.
        </p>

        <h3>2.1 Im Modul Get-Together</h3>
        <p>
            Bei Nutzung des Moduls "Get-Together" können personenbezogene Daten erhoben und verarbeitet werden. Es handelt sich dabei um Kontaktdaten (Vor- und Nachname, Bild und Email-Adresse) die auf freiwilliger Basis gespeichert werden können. Die Daten müssen in nicht-pseudonymisierter Form auf dem Server gespeichert werden damit Kontaktvorschläge anderen Nutzenden präsentiert werden können.
        </p>
        <p>
            Die Daten werden verschlüsselt auf dem Server gespeichert - nur die angebundenen Stud.IP Installationen können diese Daten wieder entschlüsseln und die Kontaktinformationen den entsprechenden Nutzenden präsentieren.
        </p>
        <p>
            Empfängerinnen und Empfänger dieser Daten sind ausschließlich die Nutzenden, denen ein Kontakt vorgeschlagen wird.
        </p>
        <p>
            Diese Daten werden verschlüsselt gespeichert und bis zum Ende der Projektlaufzeit am 30.04.2022 an der jeweiligen Universität im Backend vorgehalten und dann automatisiert gelöscht.
        </p>

        <h3>2.2 Im Modul Auslandsaufenthalt</h3>
        <p>
            Bei Nutzung des Moduls "Auslandsaufenthalt" können personenbezogene Daten erhoben und verarbeitet werden. Es handelt sich dabei um Kontaktdaten (Email-Adresse) die auf freiwilliger Basis gespeichert werden können. Sie dienen dem Empfangen von Push-Benachrichtigungen per E-Mail, die an zu erledigende nächste Schritte erinnern.
        </p>
        <p>
            Die Kontaktdaten liegen auf dem Server und können nur von den System-Administrierenden eingesehen werden.
        </p>
        <p>
            Diese Daten werden unverschlüsselt gespeichert und bis zum Ende der Projektlaufzeit am 30.04.2022 an der jeweiligen Universität im Backend vorgehalten und dann automatisiert gelöscht.
        </p>

        <h3>2.3 Im Modul Fachliche Interessen</h3>
        <p>
            Bei Nutzung des Moduls "Fachliche Interessen" können personenbezogene Daten erhoben und verarbeitet werden. Es handelt sich dabei um Kontaktdaten (Email-Adresse) die auf freiwilliger Basis gespeichert werden können. Sie dienen dem Empfangen von Push-Benachrichtigungen per E-Mail, die darauf aufmerksam machen wenn KI-Algorithmen neue persönliche Empfehlungen generiert haben.
        </p>
        <p>
            Die Kontaktdaten liegen auf dem Server und können nur von den System-Administrierenden eingesehen werden.
        </p>
        <p>
            Diese Daten werden unverschlüsselt gespeichert und bis zum Ende der Projektlaufzeit am 30.04.2022 an der jeweiligen Universität im Backend vorgehalten und dann automatisiert gelöscht.
        </p>

